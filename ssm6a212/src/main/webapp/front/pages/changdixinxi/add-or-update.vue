		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取

			// 下拉框
			res = await this.$api.option(`changdileixing`,`changdileixing`,{});
			this.changdileixingOptions = res.data;
			// 自定义下拉框值
			this.changdizhuangtaiOptions = "可预约,已预约".split(',')

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`changdixinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='changdimingcheng'){
					this.ruleForm.changdimingcheng = obj[o];
					this.ro.changdimingcheng = true;
					continue;
					}
					if(o=='changdileixing'){
					this.ruleForm.changdileixing = obj[o];
					this.ro.changdileixing = true;
					continue;
					}
					if(o=='changditupian'){
					this.ruleForm.changditupian = obj[o];
					this.ro.changditupian = true;
					continue;
					}
					if(o=='changdizhuangtai'){
					this.ruleForm.changdizhuangtai = obj[o];
					this.ro.changdizhuangtai = true;
					continue;
					}
					if(o=='changdiquyu'){
					this.ruleForm.changdiquyu = obj[o];
					this.ro.changdiquyu = true;
					continue;
					}
					if(o=='rongnarenshu'){
					this.ruleForm.rongnarenshu = obj[o];
					this.ro.rongnarenshu = true;
					continue;
					}
					if(o=='changdifeiyong'){
					this.ruleForm.changdifeiyong = obj[o];
					this.ro.changdifeiyong = true;
					continue;
					}
					if(o=='keyueshijian'){
					this.ruleForm.keyueshijian = obj[o];
					this.ro.keyueshijian = true;
					continue;
					}
					if(o=='lianxidianhua'){
