			// 多级联动参数


			// 日长控件选择日期时间
			clicktimeConfirm(val) {
				console.log(val)
				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},

			// 下拉变化
			changdileixingChange(e) {
				this.changdileixingIndex = e.target.value
				this.ruleForm.changdileixing = this.changdileixingOptions[this.changdileixingIndex]
			},
			// 下拉变化
			changdizhuangtaiChange(e) {
				this.changdizhuangtaiIndex = e.target.value
				this.ruleForm.changdizhuangtai = this.changdizhuangtaiOptions[this.changdizhuangtaiIndex]
			},

			changditupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.changditupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if((!this.ruleForm.changdimingcheng)){
					this.$utils.msg(`场地名称不能为空`);
					return
				}
				if((!this.ruleForm.changdileixing)){
					this.$utils.msg(`场地类型不能为空`);
					return
				}
				if((!this.ruleForm.changdizhuangtai)){
					this.$utils.msg(`场地状态不能为空`);
					return
				}
				if((!this.ruleForm.changdiquyu)){
					this.$utils.msg(`场地区域不能为空`);
					return
				}
				if((!this.ruleForm.changdifeiyong)){
					this.$utils.msg(`场地费用不能为空`);
					return
				}
				if(this.ruleForm.changdifeiyong&&(!this.$validate.isIntNumer(this.ruleForm.changdifeiyong))){
					this.$utils.msg(`场地费用应输入整数`);
					return
				}
				if(this.ruleForm.lianxidianhua&&(!this.$validate.isMobile(this.ruleForm.lianxidianhua))){
					this.$utils.msg(`联系电话应输入手机格式`);
					return
				}
				if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`changdixinxi`, this.ruleForm);
				}else{
					await this.$api.add(`changdixinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
